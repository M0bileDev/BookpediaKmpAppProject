@Composable
fun BlurredImageBackground(
    modifier: Modifier,
    imageUrl: String?,
    isFavorite: Boolean,
    onFavoriteClick: () -> Unit,
    onBackClick: () -> Unit,
    content: @Composable () -> Unit,
) {
    var imageLoadResult by remember {
        mutableStateOf<Result<Painter>?>(null)
    }
    val painter = rememberAsyncImagePainter(
        model = imageUrl,
        onSuccess = {
            val size = it.painter.intrinsicSize
            imageLoadResult = if (size.width > 1 && size.width > 1) {
                Result.success(it.painter)
            } else {
                Result.failure(Exception("Invalid image dimension"))
            }
        }
    )

    Box(modifier = modifier) {
        Column(modifier = Modifier.fillMaxSize()) {
            Box(
                modifier = Modifier
                    .weight(0.3f)
                    .fillMaxWidth()
                    .background(DarkBlue),
            ) {
                imageLoadResult?.getOrNull()?.let { painter ->
                    Image(
                        modifier = Modifier
                            .fillMaxSize()
                            .blur(20.dp),
                        painter = painter,
                        contentDescription = stringResource(Res.string.book_cover_hint),
                        contentScale = ContentScale.Crop,
                    )
                }
            }

            Box(
                modifier = Modifier
                    .weight(0.7f)
                    .fillMaxWidth()
                    .background(DesertWhite)
            )
        }
        IconButton(
            modifier = Modifier
                .align(Alignment.TopStart)
                .padding(16.dp)
                .statusBarsPadding(),
            onClick = onBackClick,
        ) {
            Icon(
                painter = painterResource(Res.drawable.arrow_back_24px),
                contentDescription = stringResource(Res.string.back_arrow_hint)
            )
        }
        Column(
            modifier = Modifier.fillMaxWidth(),
            horizontalAlignment = Alignment.CenterHorizontally
        ) {
            Spacer(modifier = Modifier.fillMaxHeight(fraction = 0.15f))
            ElevatedCard(
                modifier = Modifier.height(200.dp).aspectRatio(2 / 3f),
                shape = RoundedCornerShape(16.dp),
                colors = CardDefaults.elevatedCardColors(
                    containerColor = Color.Transparent
                ),
                elevation = CardDefaults.elevatedCardElevation(
                    defaultElevation = 15.dp
                )
            ) {
                AnimatedContent(
                    targetState = imageLoadResult
                ) { result ->
                    when (result) {
                        null -> CircularProgressIndicator()
                        else -> {
                            Box {
                                Image(
                                    modifier = Modifier.fillMaxSize().background(Color.Transparent),
                                    painter = if (result.isSuccess) painter else painterResource(Res.drawable.book_error_2),
                                    contentDescription = stringResource(Res.string.book_cover_hint),
                                    contentScale = if (result.isSuccess) {
                                        ContentScale.Crop
                                    } else {
                                        ContentScale.Fit
                                    }
                                )
                                IconButton(
                                    modifier = Modifier.align(Alignment.BottomEnd).background(
                                        brush = Brush.radialGradient(
                                            colors = listOf(
                                                SandYellow, Color.Transparent
                                            ),
                                            radius = 70f,
                                        )
                                    ),
                                    onClick = onFavoriteClick,
                                ) {
                                    Icon(
                                        painter = if (isFavorite) painterResource(Res.drawable.favorite_filled_24px) else painterResource(
                                            Res.drawable.favorite_24px
                                        ),
                                        contentDescription = if (isFavorite) stringResource(Res.string.remove_from_favorites) else stringResource(
                                            Res.string.add_to_favorite
                                        )
                                    )
                                }
                            }

                        }
                    }
                }
            }
        }
        content()
    }

}